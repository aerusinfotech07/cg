# ============================================
# Example 5: Print with Formatting (f-strings)
# ============================================

# f-strings (Python 3.6+) - embed variables in strings
age = 25
name = "Alex"
print(f"I am {age} years old")
print(f"My name is {name}")

# Expressions inside f-strings
a, b = 5, 10
print(f"{a} + {b} = {a + b}")

# Multiple variables
course = "Python"
level = "Beginner"
print(f"Learning {course} at {level} level")
