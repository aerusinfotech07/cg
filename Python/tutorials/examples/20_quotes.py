# ============================================
# Example 20: Single or Double Quotes
# Both work the same for strings
# ============================================

x = "John"
y = 'John'

print(x == y)  # True - they are the same

# Use the other quote when one appears in the string
msg = "It's a nice day"
quote = 'He said "Hello"'
print(msg)
print(quote)
