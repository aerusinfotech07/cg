# ============================================
# Example 19: Get the Type
# Use type() to check variable data type
# ============================================

x = 5
y = "John"
z = 3.14
flag = True

print(type(x))    # <class 'int'>
print(type(y))    # <class 'str'>
print(type(z))    # <class 'float'>
print(type(flag)) # <class 'bool'>
