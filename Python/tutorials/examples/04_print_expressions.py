# Example 4: Print Numbers and Expressions
# ============================================

# Print result of arithmetic
print(2 + 3)              # Output: 5
print(10 - 4)             # Output: 6
print(3 * 7)              # Output: 21
print(20 / 4)             # Output: 5.0

# Print expression with text
print("2 + 3 =", 2 + 3)   # Output: 2 + 3 = 5
print("10 * 5 =", 10 * 5) # Output: 10 * 5 = 50
