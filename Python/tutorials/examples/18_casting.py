# ============================================
# Example 18: Casting
# Specify data type with str(), int(), float()
# ============================================

x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0

print("str(3)  =", x, "-> type:", type(x))
print("int(3)  =", y, "-> type:", type(y))
print("float(3)=", z, "-> type:", type(z))
